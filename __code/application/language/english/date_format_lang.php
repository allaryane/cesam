$lang['df_standard']		= '%B %e, %Y - %H:%M';				// February 12 2011 - 12:34
$lang['df_long_time']		= '%A %B %e, %Y at %H:%M';			// Saturday February 12, 2011 at 12:34
$lang['df_long']			= '%A %B %e, %Y';					// Saturday February 12, 2011
$lang['df_middle_full']		= '%B %e, %Y';						// February 12, 2011
$lang['df_middle']			= '%b %e, %Y';						// Feb 12, 2011
$lang['df_short']			= '%Y-%m-%d';						// 2011-02-12
$lang['df_short_slash']		= '%Y/%m/%d';						// 2011/02/12

